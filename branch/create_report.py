from Public.log import Log
logger = Log()

def my_print(msg):
    logger.info(msg)

def save_result(testtime, toial, passnum,fail):
    try:
        f = open('result.txt', 'a')
        f.write("%s=%s=%s=%s \n" % (testtime, toial, passnum, fail))
        f.close()
    except:
        my_print('保存测试结果出错，原因：%s' % Exception)
